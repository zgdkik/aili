package org.hbhk.aili.client.core.widget.qtable.adapter;

@SuppressWarnings("serial")
public class IncorrectMethodSignatureException extends RuntimeException {

	public IncorrectMethodSignatureException() {
		super();
	}

	public IncorrectMethodSignatureException(String message, Throwable cause) {
		super(message, cause);
	}

	public IncorrectMethodSignatureException(String message) {
		super(message);
	}

	public IncorrectMethodSignatureException(Throwable cause) {
		super(cause);
	}

}
