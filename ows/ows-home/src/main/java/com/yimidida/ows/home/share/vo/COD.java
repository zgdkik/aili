package com.yimidida.ows.home.share.vo;

public class COD {
	private String waybillNo;
	private String outFeeAmt;
	private String consName;
	private String paymentState; 
	private String paymentTm;

	
	public String getPaymentState() {
		return paymentState;
	}
	public void setPaymentState(String paymentState) {
		this.paymentState = paymentState;
	}
	public String getWaybillNo() {
		return waybillNo;
	}
	public void setWaybillNo(String waybillNo) {
		this.waybillNo = waybillNo;
	}
	public String getOutFeeAmt() {
		return outFeeAmt;
	}
	public void setOutFeeAmt(String outFeeAmt) {
		this.outFeeAmt = outFeeAmt;
	}
	public String getConsName() {
		return consName;
	}
	public void setConsName(String consName) {
		this.consName = consName;
	}
	public String getPaymentTm() {
		return paymentTm;
	}
	public void setPaymentTm(String paymentTm) {
		this.paymentTm = paymentTm;
	}
	
	
	
	

}
